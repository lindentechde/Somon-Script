function ҳисоби_масофа(нуқтаи_аввал, нуқтаи_дуюм) {
  let дх = нуқтаи_дуюм[0] - нуқтаи_аввал[0];
  let ду = нуқтаи_дуюм[1] - нуқтаи_аввал[1];
  return дх * дх + ду * ду;
}
